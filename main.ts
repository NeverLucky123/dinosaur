let allGeese = [geese]
let makegeese = true
forever(function on_forever() {
    let geeseA: Sprite;
    let pause_timer: number;
    
    if (makegeese == true) {
        if (randint(0, 50) < 2 * gamespeed) {
            if (randint(0, 1) == 0) {
                geeseA = sprites.create(geese.image, 0)
                geeseA.setPosition(180, 100)
            } else {
                geeseA = sprites.create(gooseFlying.image, 0)
                geeseA.setPosition(180, 90)
            }
            
            geeseA.setVelocity(-50 * gamespeed, 0)
            allGeese.push(geeseA)
            makegeese = false
            lastgeese = game.runtime()
        }
        
    } else {
        pause_timer = (gamespeed + 7) / 5 * 700
        if (game.runtime() - lastgeese > pause_timer) {
            makegeese = true
        }
        
    }
    
    for (let geese2 of allGeese) {
        if (victim.overlapsWith(geese2)) {
            game.over(false)
        }
        
    }
    if (gamespeed < 5) {
        gamespeed = gamespeed + 0.013
    }
    
    info.changeScoreBy(Math.floor(gamespeed))
    pause(100)
})
